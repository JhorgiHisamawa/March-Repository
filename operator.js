/* Equal Operator*/

let a = 1==2 //false
let b = 1!= 2 //true

console.log(a)
console.log(b)

//Strict Equal (harus sama type datanya)
let c = "2" === 3 //false
let f = "4" === 4 //false
let g = 5 === 5 //true
console.log (c)
console.log (f)
console.log (g)

//Less strict equal
let d = "2" == 3
let e = "2" == 2
console.log(d)
console.log(e)